    private void valButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_valButtonActionPerformed
        // TODO add your handling code here:
        this.nom = nomField.getText();
        this.prenom = prenomField.getText();
        this.age = Integer.parseInt(ageField.getText());
        this.sexe = sexeField.getText();      
        this.acc = Integer.parseInt(nbreField.getText());
        
        String temp = this.nom+sep+this.prenom+sep+this.age+sep+this.sexe+sep+this.acc+sep+this.volName.toString();
                
        try {
            byte[] envoie = Securite.chiffrement(temp, sk, false);
            
            RequeteTICKMAP req = new RequeteTICKMAP(RequeteTICKMAP.REQ_BILLETS, "Verif billets",envoie);  
            
            oos = new ObjectOutputStream(sock.getOutputStream());
            oos.writeObject(req);
            oos.flush();
            
        } catch (NoSuchAlgorithmException | NoSuchProviderException | NoSuchPaddingException | IllegalBlockSizeException | BadPaddingException | InvalidKeyException | IOException ex) {
            Logger.getLogger(Application_Clients.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        this.dispose();        
    }//GEN-LAST:event_valButtonActionPerformed

